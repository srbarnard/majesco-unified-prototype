function formatIsoDate(date: Date) {
  return date.toISOString().slice(0, 10)
}

function relativeTimeFromDate(date: Date, referenceDate = new Date('2026-05-13T12:00:00')) {
  const diffMs = referenceDate.getTime() - date.getTime()
  const diffDays = Math.floor(diffMs / (1000 * 60 * 60 * 24))

  if (diffDays <= 0) return 'Today'
  if (diffDays === 1) return '1d ago'
  if (diffDays < 7) return `${diffDays}d ago`
  if (diffDays < 14) return '1w ago'
  if (diffDays < 30) return `${Math.floor(diffDays / 7)}w ago`
  if (diffDays < 60) return '1mo ago'
  return `${Math.floor(diffDays / 30)}mo ago`
}

function buildGeneratedDocuments(referenceDate = new Date('2026-05-13T12:00:00')): PolicyDocument[] {
  return generatedDocumentTemplates.map((template, index) => {
    const date = new Date(referenceDate)
    date.setDate(date.getDate() - (index + 1) * 2 - (index % 3))

    return {
      ...template,
      id: `doc-gen-${index + 7}`,
      date: formatIsoDate(date),
      relativeTime: relativeTimeFromDate(date, referenceDate),
    }
  })
}

